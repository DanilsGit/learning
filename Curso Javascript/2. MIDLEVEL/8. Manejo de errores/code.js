
/* Sentencia switch */

let key = "xd";

switch (key) {
    case "banana": 
        console.log("Es una banana"); 
        break;
    case "pera": 
        console.log("Es una pera"); 
        break;
    case "manzana": 
        console.log("Es una manzana"); 
        break;

    default: 
        console.log("No es ninguna fruta");
        break;
}